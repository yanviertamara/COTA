    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        ControladorArchivos ca = new ControladorArchivos();

        JFileChooser fc = new JFileChooser();
        //Mostrar la ventana para abrir archivo y recoger la respuesta
        //En el par치metro del showOpenDialog se indica la ventana
        //  al que estar치 asociado. Con el valor this se asocia a la
        //  ventana que la abre.
        File path = null;
        fc.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        int respuesta = fc.showOpenDialog(this);
        //Comprobar si se ha pulsado Aceptar
        if (respuesta == JFileChooser.APPROVE_OPTION) {
            //Crear un objeto File con el archivo elegido
            File archivoElegido = fc.getSelectedFile();
            //Mostrar el nombre del archvivo en un campo de texto
            jTxtPath.setText(archivoElegido.getAbsolutePath());
            path = archivoElegido;
        }
 
        ca.listFiles(path);
    }//GEN-LAST:event_jButton1ActionPerformed

